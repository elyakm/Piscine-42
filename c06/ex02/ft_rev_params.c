#include <unistd.h>

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
	write(1, "\n", 1);
}

int	main(int argc, char **argv)
{
	int	a;

	a = argc - 1;
	while (a >= 1)
	{
		ft_putstr(argv[a]);
		a--;
	}
	return (0);
}
