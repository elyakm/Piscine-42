int	ft_atoi(char *str)
{
	int	i;
	int	conv;
	int	resp;

	i = 0;
	conv = 1;
	resp = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
	{
		i++;
	}
	while (str[i] == '-' || str[i] == '+')
	{
		conv *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		resp = (str[i] - '0') + (resp * 10);
		i++;
	}
	return (resp * conv);
}
